package com.ue.insw.proyecto.exercises.ej7json;

public class Propiedades {
    private String id;
    private String name;

    public Propiedades(String id, String name) {
        this.id = id;
        this.name = name;
    }

    public String getId() {
        return id;
    }

    public String getName() {
        return name;
    }
}
